create table COMPANY(
   COMPANYID VARCHAR(300) NOT NULL PRIMARY KEY,
   COMPANYNAME VARCHAR(300) NOT NULL,
   COMPANYREGISTEREDADDRESS VARCHAR(300) NOT NULL,
   COMPANYACCOUNTADDRESS VARCHAR(300) NOT NULL,
);

create table BANK(
   BANKID VARCHAR(300) NOT NULL PRIMARY KEY,
   BANKACCOUNTADDRESS VARCHAR(300) NOT NULL,
   BANKNAME VARCHAR(100) NOT NULL,
   CITY VARCHAR(50) NOT NULL
);

create table COLLECTIONAGENCY(
   COLLECTIONAGENCYID VARCHAR(300) NOT NULL PRIMARY KEY,
   COLLECTIONAGENCYNAME VARCHAR(300) NOT NULL,
   COLLECTIONAGENCYACCOUNTADDRESS VARCHAR(100) NOT NULL
);

create table TRANSACTIONB2C(
   TRANSACTIONID VARCHAR(300) NOT NULL PRIMARY KEY,
   COMPANYID VARCHAR(300) NOT NULL,
   BANKID VARCHAR(100) NOT NULL,
   BANKACCOUNTNUMBER VARCHAR(50) NOT NULL,
   AMOUNTDUE INT,
   INSTALLMENTAMOUNT INT ,
   DUEDATE DATETIME,
   DAYSINARREAR INT,
   PAYMENTDATE  DATETIME,
   TRANSACTIONTYPE INT,
   ACCOUNTTYPE  INT,
   ACCOUNTBALANCE INT
);

